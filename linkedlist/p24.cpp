class Solution
{
    public:
    Node* reverse(Node* head)
    {
        if (head == NULL || head->next == NULL)
            return head;
 
        /* reverse the rest list and put
          the first element at the end */
        Node* rest = reverse(head->next);
        head->next->next = head;
 
        /* tricky step -- see the diagram */
        head->next = NULL;
 
        /* fix the head pointer */
        return rest;
    }
    Node *compute(Node *head)
    {
        // your code goes here
        if(head->next==NULL)
            return head;
            
        head=reverse(head);
        int max=head->data;
        Node* ptr=head;
        Node* prev;
        while(ptr!=NULL){
            if(max>ptr->data){
                prev->next=ptr->next;
                
                ptr=ptr->next;
            }
            else{
                max=ptr->data;
                prev=ptr;
                ptr=ptr->next;
                
            }
            
        }
        head=reverse(head);
        return head;
    }
    
};
   


